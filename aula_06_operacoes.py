# Operadores Relaconais e Prêcedencia

# exemplos de operadores:
# maior que >
# menor que <
# maior ou igual >=
# menor ou igual <=
# igual a ==
# diferente de !=

valorA = int(input("Qual o valor A?"))
valorB = int(input("Qual o valor B?"))

print(valorA > valorB)
print(valorA < valorB)
print(valorA >= valorB)
print(valorA <= valorB)
print(valorA == valorB)
print(valorA != valorB)



